class Doctor {
  final int id;
  final String name;
  final String specialty;
  final String icon;
  final String bio;
  final int yearsOfExperience;
  final String status;
  final String tenantName;

  Doctor({
    required this.id,
    required this.name,
    required this.specialty,
    required this.icon,
    required this.bio,
    required this.yearsOfExperience,
    required this.status,
    required this.tenantName,
  });
}
